-- Discord webhook generator (For funny)
function generateRandomWebhook()
    local randomNumber = math.random(1000000000, 9999999999)
    local randomString = ""
    local letters = "abcdefghijklmnopqrstuvwxyzABCDEFGHIJKLMNOPQRSTUVWXYZ0123456789"

    for i = 1, 43 do
        local index = math.random(1, #letters)
        randomString = randomString..letters:sub(index, index)
    end
    
    return string.format("https://discord.com/api/webhooks/%d/%s", randomNumber, randomString)
end

